#Soal No 3 Test Case 1
#Menghitung semen yang dibutuhkan
#Rumus luas gabungan = rumus luas segitiga + rumus luas persegi

a = int(input("Masukkan alas atap: "))
ta = int(input("Masukkan tinggi atap: "))
tt = int(input("Masukkan tinggi tembok: "))

luas = (a * ta * 0.5) + (tt * tt)
semen = luas / 5

print("rumah tersebut membutuhkan", semen, "sak semen")

